export async function getServerSideProps() {
  const client = createClient({
    projectId:"1yohlwwu",
    dataset:"production",
    useCdn:false
  })
  const query = `*[_type == 'post']`
  const works = await client.fetch(query)
  return { 
    props: { 
      works
    }
  }
}
